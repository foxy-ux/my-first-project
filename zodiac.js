function findZodiac() {
    const date = new Date(document.getElementById('birthDate').value);
    const day = date.getDate();
    const month = date.getMonth() + 1;
    
    if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) {
        showResult("Водолей — бунтарь и гуманист — оригинальный и независимый — мыслитель будущего — дружит со всеми, но принадлежит себе.");
    } else if ((month === 2 && day >= 19) || (month === 3 && day <= 20)) {
        showResult("Рыбы — мистик и эмпат — мечтательный и растворяющийся — живёт в мире чувств и фантазий — границы для них условны.");
    } else if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) {
        showResult("Овен — первопроходец и воин — импульсивный и энергичный — всегда в движении — прямолинейный как таран.");
    } else if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) {
        showResult("Телец — утончённый эпикуреец и собственник — ценитель комфорта и стабильности — медленный, но неумолимый — его кредо «владею, значит, существую».");
    } else if ((month === 5 && day >= 21) || (month === 6 && day <= 21)) {
        showResult("Близнецы — вечный подросток и коммуникатор — лёгкий на подъём и переменчивый — мастер слов и информации — внутри него живут два человека, а то и больше.");
    } else if ((month === 6 && day >= 22) || (month === 7 && day <= 22)) {
        showResult("Рак — чуткий защитник и домосед — ранимый и заботливый — живёт эмоциями и памятью — его панцирь скрывает нежную душу.");
    } else if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) {
        showResult("Лев — королевская особа и артист — щедрый и тщеславный — обожает центр внимания — греет окружающих своим внутренним солнцем.");
    } else if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) {
        showResult("Дева — аналитик и перфекционист — практичный и критичный — видит детали, которые другие не замечают — служит миру через работу и заботу.");
    } else if ((month === 9 && day >= 23) || (month === 10 && day <= 23)) {
        showResult("Весы — эстет и дипломат — обаятельный и нерешительный — ищет гармонию и партнёрство — ненавидит конфликты и одиночество.");
    } else if ((month === 10 && day >= 24) || (month === 11 && day <= 22)) {
        showResult("Скорпион — магнетизм и трансформация — страстный и ревнивый — видит всё насквозь — рождается в кризисе, чтобы стать сильнее.");
    } else if ((month === 11 && day >= 23) || (month === 12 && day <= 21)) {
        showResult("Стрелец — искатель истины и авантюрист — оптимистичный и прямолинейный — его девиз «меньше слов, больше дел» — вечно смотрит на горизонт.");
    } else {
        showResult("Козерог — стратег и аскет — амбициозный и дисциплинированный — медленно, но верно карабкается на вершину — уважает традиции и иерархию.");
    }
}

function showResult(sign) {
    document.getElementById('result').innerHTML = "<h3>Ваш знак: " + sign + "</h3>";
}